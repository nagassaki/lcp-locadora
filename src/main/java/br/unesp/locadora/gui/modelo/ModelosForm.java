    private void tabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelaMouseClicked

        if (tabela.rowAtPoint(evt.getPoint()) > -1) {
            btnEditar.setEnabled(true);
            btnExcluir.setEnabled(true);
        }

        if (evt.getClickCount() == 2) {
            btnEditarActionPerformed(null);
        }
    }//GEN-LAST:event_tabelaMouseClicked

    private void scrollMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_scrollMouseClicked
        removerSelecao();
    }//GEN-LAST:event_scrollMouseClicked

    /**
     * Remove seleção da tabela.
     */
    private void removerSelecao() {
        tabela.clearSelection();
        btnEditar.setEnabled(false);
        btnExcluir.setEnabled(false);
    }

    /**
     * Atualiza dados da tabela.
     *
     * @param dados Dados.
     */
    private void atualizarTabela(List<Modelo> dados) {
        JTableUtil.fillModelos(tabela, dados);
        removerSelecao();
    }

    /**
     * Atualiza dados da tabela.
     */
    private void atualizarTabela() {
        atualizarTabela(modeloService.list());
    }

    /**
     * Retorna ID do modelo selecionado.
     *
     * @return ID do modelo.
     */
    private int idLinhaSelecionada() {

        int id = -1;

        if (tabela.getSelectedRow() != -1) {
            id = Integer.parseInt(tabela.getModel().getValueAt(tabela.getSelectedRow(), 0).toString());
        }

        return id;
    }

    /**
     * Remove linha selecionada da tabela.
     */
    private void removerLinhaSelecionada() {
        JTableUtil.remove(tabela, tabela.getSelectedRow());
    }

