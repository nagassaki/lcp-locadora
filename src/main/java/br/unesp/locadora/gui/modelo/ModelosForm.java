    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed

        if (idLinhaSelecionada() != -1) {

            EditarModeloForm form = new EditarModeloForm();

            form.addWindowListener(new WindowAdapter() {

                @Override
                public void windowClosed(WindowEvent evt) {
                    atualizarTabela();
                }

            });

            form.setId(idLinhaSelecionada());
            form.setVisible(true);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        if (idLinhaSelecionada() != -1) {
            if (JOptionPane.showConfirmDialog(this, "Deseja realmente excluir o modelo selecionado?", "Aviso", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                try {
                    modeloService.delete(modeloService.getById(idLinhaSelecionada()));
                    removerLinhaSelecionada();
                    removerSelecao();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnExcluirActionPerformed

