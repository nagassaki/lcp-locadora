    private void btnPesquisar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisar1ActionPerformed

        if (!txtCep1.getText().trim().equals("")) {

            Endereco endereco = CEP.getEndereco(txtCep1.getText().trim());

            if (endereco != null) {
                cbxEstado1.setSelectedItem(endereco.getEstado());
                txtCidade1.setText(endereco.getCidade());
                txtBairro1.setText(endereco.getBairro());
                txtEndereco1.setText(endereco.getEndereco());
            } else {
                JOptionPane.showMessageDialog(this, "Não foi possível obter o endereço.", "Aviso", JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Informe um CEP.", "Aviso", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnPesquisar1ActionPerformed

    private void btnAtualizar1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizar1ActionPerformed
        try {

            Fisica cliente = fisicaService.getById(id);

            cliente.setNome(txtNome.getText().trim());
            cliente.setCpf(txtCpf.getText().trim());
            cliente.setRg(txtRg.getText().trim());
            cliente.setTelefone(txtTelefone1.getText().trim());
            cliente.setEmail(txtEmail1.getText().trim());
            cliente.setEndereco(new Endereco(txtCep1.getText().trim(), cbxEstado1.getSelectedItem().toString(), txtCidade1.getText().trim(), txtBairro1.getText().trim(), txtEndereco1.getText().trim(), Integer.parseInt((txtNumero1.getText().trim().equals("") ? "0" : txtNumero1.getText())), txtComplemento1.getText().trim()));

            if (txtNascimento.getDate() != null) {
                cliente.setDataNascimento(txtNascimento.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate());
            } else {
                cliente.setDataNascimento(null);
            }

            fisicaService.save(cliente);

            JOptionPane.showMessageDialog(this, "Cliente atualizado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Informe um número válido!", "Aviso", JOptionPane.WARNING_MESSAGE);
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Aviso", JOptionPane.WARNING_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAtualizar1ActionPerformed

