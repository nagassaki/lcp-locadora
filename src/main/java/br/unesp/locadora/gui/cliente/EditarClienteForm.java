    public void setIdFisica(int id) {
        if (id > 0) {
            this.id = id;
            tabbed.setSelectedIndex(0);
            tabbed.setEnabledAt(1, false);
            preencherFormularioFisica();
        }
    }

    public void setIdJuridica(int id) {
        if (id > 0) {
            this.id = id;
            tabbed.setSelectedIndex(1);
            tabbed.setEnabledAt(0, false);
            preencherFormularioJuridica();
        }
    }

