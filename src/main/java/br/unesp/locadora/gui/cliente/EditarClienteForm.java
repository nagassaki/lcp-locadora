    /**
     * Preenche formulário de cliente pessoa física.
     */
    private void preencherFormularioFisica() {

        Fisica cliente = fisicaService.getById(id);

        txtNome.setText(cliente.getNome());
        txtCpf.setText(cliente.getCpf());
        txtRg.setText(cliente.getRg());
        txtTelefone1.setText(cliente.getTelefone());
        txtEmail1.setText(cliente.getEmail());
        txtCep1.setText(cliente.getEndereco().getCep());
        cbxEstado1.setSelectedItem(cliente.getEndereco().getEstado());
        txtCidade1.setText(cliente.getEndereco().getCidade());
        txtBairro1.setText(cliente.getEndereco().getBairro());
        txtEndereco1.setText(cliente.getEndereco().getEndereco());
        txtNumero1.setText(Integer.toString(cliente.getEndereco().getNumero()));
        txtComplemento1.setText(cliente.getEndereco().getComplemento());

        if (cliente.getDataNascimento() != null) {
            txtNascimento.setDate(new Date().from(cliente.getDataNascimento().atStartOfDay().atZone(ZoneId.systemDefault()).toInstant()));
        } else {
            txtNascimento.setDate(null);
        }
    }

