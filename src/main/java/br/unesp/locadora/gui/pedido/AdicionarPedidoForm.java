    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        try {

            Pedido pedido = new Pedido();

            pedido.setData(LocalDateTime.now());
            pedido.setVeiculo((Veiculo) cbxVeiculo.getSelectedItem());
            pedido.setCliente((Cliente) cbxCliente.getSelectedItem());
            pedido.setRetirada(txtRetirada.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDateTime());
            pedido.setDevolucao(txtDevolucao.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDateTime());
            pedido.calcular();

            pedidoService.save(pedido);
            limparFormulario();

            JOptionPane.showMessageDialog(this, "Pedido adicionado com sucesso!", "Sucesso", JOptionPane.INFORMATION_MESSAGE);
        } catch (IllegalArgumentException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Aviso", JOptionPane.WARNING_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAdicionarActionPerformed

