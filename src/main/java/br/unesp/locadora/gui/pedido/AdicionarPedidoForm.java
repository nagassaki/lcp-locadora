    /**
     * Atualiza lista de veículos.
     */
    private void atualizarVeiculos() {
        JComboBoxUtil.fillVeiculos(cbxVeiculo, veiculoService.list());
    }

    /**
     * Atualiza lista de clientes.
     */
    private void atualizarClientes() {

        List<Cliente> clientes = new ArrayList<>();

        fisicaService.list().forEach(cliente -> {
            clientes.add(cliente);
        });
        juridicaService.list().forEach(cliente -> {
            clientes.add(cliente);
        });

        JComboBoxUtil.fillClientes(cbxCliente, clientes);
    }

    /**
     * Carrega informações do formulário.
     */
    private void carregarFormulario() {
        atualizarVeiculos();
        atualizarClientes();
    }

    /**
     * Limpa formulário.
     */
    private void limparFormulario() {

        txtRetirada.setDate(null);
        txtDevolucao.setDate(null);

        if (cbxVeiculo.getItemCount() > 0) {
            cbxVeiculo.setSelectedIndex(0);
        }

        if (cbxCliente.getItemCount() > 0) {
            cbxCliente.setSelectedIndex(0);
        }
    }

