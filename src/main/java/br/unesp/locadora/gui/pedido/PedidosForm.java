    /**
     * Remove seleção da tabela de pedidos ativos.
     */
    private void removerSelecaoAtivos() {
        tblAtivos.clearSelection();
        btnEditar.setEnabled(false);
        btnFinalizar.setEnabled(false);
        btnCancelar.setEnabled(false);
    }

    /**
     * Remove seleção da tabela de pedidos finalizados.
     */
    private void removerSelecaoFinalizados() {
        tblFinalizados.clearSelection();
        btnVisualizar2.setEnabled(false);
    }

    /**
     * Remove seleção da tabela de pedidos cancelados.
     */
    private void removerSelecaoCancelados() {
        tblCancelados.clearSelection();
        btnVisualizar3.setEnabled(false);
    }

    /**
     * Atualiza dados da tabela de pedidos ativos.
     *
     * @param dados Dados.
     */
    private void atualizarTabelaAtivos(List<Pedido> dados) {
        JTableUtil.fillPedidos(tblAtivos, dados, true);
        removerSelecaoAtivos();
    }

    /**
     * Atualiza dados da tabela de pedidos ativos.
     */
    private void atualizarTabelaAtivos() {
        atualizarTabelaAtivos(pedidoService.listAtivos());
    }

    /**
     * Atualiza dados da tabela de pedidos finalizados.
     *
     * @param dados Dados.
     */
    private void atualizarTabelaFinalizados(List<Pedido> dados) {
        JTableUtil.fillPedidos(tblFinalizados, dados, false);
        removerSelecaoFinalizados();
    }

    /**
     * Atualiza dados da tabela de pedidos finalizados.
     */
    private void atualizarTabelaFinalizados() {
        atualizarTabelaFinalizados(pedidoService.listFinalizados());
    }

