    private void btnCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelarActionPerformed
        if (idLinhaSelecionada(tblAtivos) != -1) {
            if (JOptionPane.showConfirmDialog(this, "Deseja realmente cancelar o pedido selecionado?", "Aviso", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
                try {

                    Pedido pedido = pedidoService.getById(idLinhaSelecionada(tblAtivos));
                    pedido.cancelar();

                    pedidoService.save(pedido);

                    removerLinhaSelecionada(tblAtivos);
                    removerSelecaoAtivos();
                    atualizarTabelaCancelados();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                }
            }
        }
    }//GEN-LAST:event_btnCancelarActionPerformed

